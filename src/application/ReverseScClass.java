package application;

public class ReverseScClass {
	
	int num;
	String letter;
	
	public ReverseScClass(int num, String letter) {
		this.num = num;
		this.letter = letter;
	}
	
	public int getNum() {
		return num;
	}
	
	public void setNum(int num) {
		this.num = num;
	}
	
	public String getLetter() {
		return letter;
	}
	
	public void setLetter(String letter) {
		this.letter = letter;
	}
	
}
